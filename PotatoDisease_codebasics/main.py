def _main():
    while True:
        print(
            "####################################################################################################\n"
            "####################################################################################################\n"
            "                                    What do you want to do:\n"
            "                                    1) Train New Model\n"
            "                                    2) Run Api\n"
        )

        response = input()

        try:
            response = int(response)
        except ValueError:
            print("Invalid Response\n\n\n\n\n")
            continue

        if not (3 > response > 0):
            print("Invalid Response\n\n\n\n\n")
            continue

        if response == 1:
            print(
                "####################################################################################################\n"
                "######################################## Training New Model ########################################\n"
                "####################################################################################################\n"
            )
            from training.training import main as train
            train()
        elif response == 2:
            print(
                "####################################################################################################\n"
                "########################################### Running API ############################################\n"
                "####################################################################################################\n"
            )
            from api.api import run_api
            run_api()

        break


if __name__ == '__main__':
    _main()